package model;

/**This class is used to save all the outputs as instances
 * */
public class Line {

	public String text;
	
	/**<p>
	 * The constructor method of the {@link Line} class
	 * </p>
	 * <p>
	 * It creates a new instance of the line class with the provided parameters
	 * </p>
	 * @param txt The text that has to be saved as a line
	 * */
	public Line(String txt) {
		text = txt;
	}
	
}
